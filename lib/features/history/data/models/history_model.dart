import '../../domain/entities/history.dart';

class HistoryModel extends History {
  HistoryModel({
    final int date,
    final int duration,
    final String friendlyName,
    final String fullTitle,
    final int grandparentRatingKey,
    final String grandparentTitle,
    final int groupCount,
    final List<int> groupIds,
    final int id,
    final String ipAddress,
    final int live,
    final int mediaIndex,
    final String mediaType,
    final int parentMediaIndex,
    final int parentRatingKey,
    final String parentTitle,
    final int pausedCounter,
    final int percentComplete,
    final String platform,
    final String player,
    final String product,
    final int ratingKey,
    final int sessionKey,
    final int started,
    final String state,
    final int stopped,
    final String title,
    final String thumb,
    final String transcodeDecision,
    final String user,
    final int userId,
    final num watchedStatus,
    final int year,
    String posterUrl,
  }) : super(
          date: date,
          duration: duration,
          friendlyName: friendlyName,
          fullTitle: fullTitle,
          grandparentRatingKey: grandparentRatingKey,
          grandparentTitle: grandparentTitle,
          groupCount: groupCount,
          groupIds: groupIds,
          id: id,
          ipAddress: ipAddress,
          live: live,
          mediaIndex: mediaIndex,
          mediaType: mediaType,
          parentMediaIndex: parentMediaIndex,
          parentRatingKey: parentRatingKey,
          parentTitle: parentTitle,
          pausedCounter: pausedCounter,
          percentComplete: percentComplete,
          platform: platform,
          player: player,
          product: product,
          ratingKey: ratingKey,
          sessionKey: sessionKey,
          started: started,
          state: state,
          stopped: stopped,
          title: title,
          thumb: thumb,
          transcodeDecision: transcodeDecision,
          user: user,
          userId: userId,
          watchedStatus: watchedStatus,
          year: year,
          posterUrl: posterUrl,
        );

  factory HistoryModel.fromJson(Map<String, dynamic> json) {
    List<int> groupIds = [];
    json['group_ids']?.split(',')?.forEach((id) => groupIds.add(int.parse(id)));

    return HistoryModel(
      date: json['date'],
      duration: json['duration'],
      friendlyName: json['friendly_name'],
      fullTitle: json['full_title'],
      grandparentRatingKey: json['grandparent_rating_key'] == ''
          ? null
          : json['grandparent_rating_key'],
      grandparentTitle: json['grandparent_title'],
      groupCount: json['group_count'],
      groupIds: groupIds,
      id: json['id'],
      ipAddress: json['ip_address'],
      live: json['live'],
      mediaIndex: json['media_index'] == '' ? null : json['media_index'],
      mediaType: json['media_type'],
      parentMediaIndex:
          json['parent_media_index'] == '' ? null : json['parent_media_index'],
      parentRatingKey:
          json['parent_rating_key'] == '' ? null : json['parent_rating_key'],
      parentTitle: json['parent_title'],
      pausedCounter: json['paused_counter'],
      percentComplete: json['percent_complete'],
      platform: json['platform'],
      player: json['player'],
      product: json['product'],
      ratingKey: json['rating_key'],
      sessionKey: json['session_key'],
      started: json['started'],
      state: json['state'],
      stopped: json['stopped'],
      title: json['title'],
      thumb: json['thumb'],
      transcodeDecision: json['transcode_decision'],
      user: json['user'],
      userId: json['user_id'],
      watchedStatus: json['watched_status'],
      year: json['year'] == '' ? null : json['year'],
    );
  }
}
