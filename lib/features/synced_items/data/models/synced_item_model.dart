import 'package:tautulli_remote/features/synced_items/domain/entities/synced_item.dart';

class SyncedItemModel extends SyncedItem {
  SyncedItemModel({
    // int audioBitrate,
    String clientId,
    // String contentType,
    String deviceName,
    // String failure,
    // int itemDownloadedCount,
    // int itemDownloadedPercentComplete,
    int itemCompleteCount,
    // int itemCount,
    String mediaType,
    // int photoBitrate,
    String platform,
    int ratingKey,
    String rootTitle,
    String state,
    int syncId,
    String syncTitle,
    int totalSize,
    String user,
    int userId,
    String username,
    // int videoBitrate,
    int videoQuality,
    String posterUrl,
  }) : super(
          // audioBitrate: audioBitrate,
          clientId: clientId,
          // contentType: contentType,
          deviceName: deviceName,
          // failure: failure,
          // itemDownloadedCount: itemDownloadedCount,
          // itemDownloadedPercentComplete: itemDownloadedPercentComplete,
          itemCompleteCount: itemCompleteCount,
          // itemCount: itemCount,
          mediaType: mediaType,
          // photoBitrate: photoBitrate,
          platform: platform,
          ratingKey: ratingKey,
          rootTitle: rootTitle,
          state: state,
          syncId: syncId,
          syncTitle: syncTitle,
          totalSize: totalSize,
          user: user,
          userId: userId,
          username: username,
          // videoBitrate: videoBitrate,
          videoQuality: videoQuality,
          posterUrl: posterUrl,
        );

  factory SyncedItemModel.fromJson(Map<String, dynamic> json) {
    return SyncedItemModel(
      clientId: json['client_id'],
      deviceName: json['device_name'],
      itemCompleteCount: int.tryParse(json['item_complete_count']),
      mediaType: json['metadata_type'],
      platform: json['platform'],
      ratingKey: int.tryParse(json['rating_key']),
      rootTitle: json['root_title'],
      state: json['state'],
      syncId: int.tryParse(json['sync_id']),
      syncTitle: json['sync_title'],
      totalSize: int.tryParse(json['total_size']),
      user: json['user'],
      userId: int.tryParse(json['user_id']),
      username: json['username'],
      videoQuality: int.tryParse(json['video_quality']),
    );
  }
}
